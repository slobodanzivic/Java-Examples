package IspisBrojevaUnazad;

public class DemoClass1 {

	public static void main(String[] args) {
		// Napisati program za ispis kvadrata prirodnih brojeva od 1 do 10.

		for (int i = 1; i <= 10; i++) {
			System.out.println("Broj je :" + i);
			System.out.println("Kvadrat broja " + i + " je: " + i * i);
		}
	}

}
