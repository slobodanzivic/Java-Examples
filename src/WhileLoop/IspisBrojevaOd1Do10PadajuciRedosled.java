package WhileLoop;

public class IspisBrojevaOd1Do10PadajuciRedosled {

	public static void main(String[] args) {
		// Ispisati prirodne brojeva od 1 do 10 u opadajucem redosledu .
		// Zadatak uraditi koriscenjem WHILE petlje.

		int i = 10;
		while (i >= 1) {
			System.out.println(i);
			i--;
		}
	}
}
